package com.realgotqkura.utilities;

public class LWColor {

    private final int r;
    private final int g;
    private final int b;

    public LWColor(int r, int g, int b){
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public int getRed(){
        return this.r;
    }

    public int getGreen(){
        return this.g;
    }

    public int getBlue(){
        return this.b;
    }
}
